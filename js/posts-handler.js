// --- ပင်မစာမျက်နှာ (index.html) အတွက် သီးသန့် ---
function loadLatestPosts() {
    const container = document.getElementById('latestBlogContainer');
    if (!container) return;

    const latest = [...window.myPosts].reverse().slice(0, 3);
    container.innerHTML = '';
    latest.forEach(post => {
        container.innerHTML += `
            <div class="col-lg-4 col-md-6 mb-4" data-aos="fade-up">
                <div class="feature-card h-100 p-0" style="overflow: hidden; border-radius: 12px; border: 1px solid #e1e5eb;">
                    <img src="${post.cover}" style="width: 100%; height: 200px; object-fit: cover;">
                    <div style="padding: 20px;">
                        <span class="badge bg-primary mb-2">${post.category}</span>
                        <h5 style="font-weight: 700;">${post.title}</h5>
                        <p class="small text-muted">${post.excerpt}</p>
                        <a href="single-blog.html?id=${post.id}" class="text-primary fw-bold" style="text-decoration: none;">ဖတ်ရှုရန် →</a>
                    </div>
                </div>
            </div>`;
    });
}

// --- ဘလော့ဂ်စာမျက်နှာ (blog.html) အတွက် Category ခေါင်းစဉ်များဖြင့် ပြသရန် ---
function loadAllPosts() {
    const container = document.getElementById('blogContainer');
    if (!container) return;

    container.innerHTML = '';
    
    // ရေးသားထားသော ဆောင်းပါးများထဲမှ ပါဝင်နေသော Category အားလုံးကို ဆွဲထုတ်ခြင်း
    const categories = [...new Set(window.myPosts.map(post => post.category))];

    if (categories.length === 0) {
        container.innerHTML = `<h5 class="text-center text-muted py-5">ဆောင်းပါးများ မရှိသေးပါ။</h5>`;
        return;
    }

    // Category တစ်ခုချင်းစီအတွက် ခေါင်းစဉ်နှင့် ဆောင်းပါးများကို ထုတ်ပြခြင်း
    categories.forEach(category => {
        const catPosts = window.myPosts.filter(post => post.category === category);
        if(catPosts.length === 0) return;

        // ၁။ Category ခေါင်းစဉ် (Heading) ထည့်သွင်းခြင်း
        container.innerHTML += `
            <div class="col-12 mt-5 mb-4" data-aos="fade-up">
                <h3 style="color: #8a2be2; border-bottom: 2px solid #00d2ff; padding-bottom: 10px; font-weight: 800;">
                    <i class="ri-folder-open-fill align-middle me-2"></i> ${category}
                </h3>
            </div>
        `;

        // ၂။ ထိုခေါင်းစဉ်အောက်ရှိ ဆောင်းပါးများကို ထည့်သွင်းရန် Row အစ
        let htmlString = `<div class="row g-4">`;
        
        catPosts.forEach(post => {
            htmlString += `
                <div class="col-lg-4 col-md-6" data-aos="fade-up">
                    <div class="chart-card p-0" style="overflow: hidden; background: #151a23; border: 1px solid #232b38; border-radius: 12px; transition: 0.3s; display: flex; flex-direction: column; height: 100%;">
                        <img src="${post.cover}" style="width: 100%; height: 220px; object-fit: cover;">
                        <div class="p-4 d-flex flex-column flex-grow-1">
                            <small class="d-block mb-2" style="color: #00d2ff; font-weight: 600;">${post.date}</small>
                            <h5 style="font-weight: 700; color: #e0e6ed; font-size: 17px; margin-bottom: 10px;">${post.title}</h5>
                            <p class="small flex-grow-1" style="color: #8b94a5; line-height: 1.6;">${post.excerpt}</p>
                            <a href="single-blog.html?id=${post.id}" style="display: block; text-align: center; text-decoration: none; padding: 12px; border-radius: 8px; background: linear-gradient(135deg, #8a2be2, #00d2ff); color: #fff; font-weight: 700; margin-top: 15px;">
                                အပြည့်အစုံဖတ်ရန်
                            </a>
                        </div>
                    </div>
                </div>`;
        });
        
        // Row အဆုံးသတ်
        htmlString += `</div>`;
        container.innerHTML += htmlString;
    });
}

// --- ဆောင်းပါးအပြည့်အစုံ (single-blog.html) ပြသရန် ---
function loadSinglePost() {
    const container = document.getElementById('postDetail');
    if (!container) return;

    const params = new URLSearchParams(window.location.search);
    const id = params.get('id');
    const post = window.myPosts.find(p => p.id === id);

    if (post) {
        document.title = `${post.title} - Daw Thin Yanant`;
        container.innerHTML = `
            <img src="${post.cover}" class="img-fluid rounded mb-4 w-100" style="max-height: 400px; object-fit: cover;">
            <div class="badge bg-primary mb-2" style="background-color: #8a2be2 !important;">${post.category}</div>
            <h1 class="fw-bold mb-3" style="color: #4b2354;">${post.title}</h1>
            <p class="text-muted small">ရက်စွဲ - ${post.date} | Tags: ${post.tags}</p>
            <hr>
            <div style="font-size: 1.1rem; line-height: 1.9; color: #333; margin-top: 20px;">
                ${post.content}
            </div>
        `;
    } else {
        container.innerHTML = `<h3 class="text-center py-5">ဆောင်းပါး ရှာမတွေ့ပါ။</h3>`;
    }
}

// စာမျက်နှာ အလုပ်လုပ်သည်နှင့် သက်ဆိုင်ရာ Function ကိုသာ ခေါ်မည်
document.addEventListener('DOMContentLoaded', () => {
    if (document.getElementById('latestBlogContainer')) {
        loadLatestPosts();
    }
    if (document.getElementById('blogContainer')) {
        loadAllPosts();
    }
    if (document.getElementById('postDetail')) {
        loadSinglePost();
    }
});